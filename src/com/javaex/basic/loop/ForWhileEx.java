package com.javaex.basic.loop;



public class ForWhileEx {

	public static void main(String[] args) {
		
	
		int num = 1; int dan=1;
		
		while(num <=9) 
		while(dan <=9) {
			
			num++;
			dan++;
			System.out.println(dan + "*" + num + "=" + (dan * num));
			
		
		
		
		}

	}

}


